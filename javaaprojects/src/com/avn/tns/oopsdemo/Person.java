package com.avn.tns.oopsdemo;

public class Person {

    private int pid;
    private String name;
    private int age;

    // Setters
    public void setPid(int pid) {
        this.pid = pid;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setAge(int age) {
        this.age = age;
    }

    // Getters
    public int getPid() {
        return pid;
    }
    public String getName() {
        return name;
    }
    public int getAge() {
        return age;
    }
}
